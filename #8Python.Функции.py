# Задача 5: Декораторы
# Создайте простой декоратор с именем `my_decorator`.
# Декоратор должен делать следующее:
# 1) Перед вызовом декорируемой функции печатать строку: "Функция вызвана".
# 2) Вызывать саму декорируемую функцию.
# 3) После вызова декорируемой функции печатать строку: "Вызов функции завершен".
# Примените этот декоратор к функции `example`, которая просто печатает "Привет!".
# Вызовите декорированную функцию `example`.
def my_decorator(func):
    def wrapper():
        print("Функция вызвана")
        func()
        print("Вызов функции завершен")
    return wrapper
@my_decorator
def example():
        print("Привет!")
example()

