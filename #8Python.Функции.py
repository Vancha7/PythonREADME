# Задача 9: Декораторы
# Создайте декоратор с именем `repeat_three_times`.
# Декоратор должен задекорированную функцию вызывать ровно ТРИ раза подряд.
# Сам декоратор не должен ничего печатать.
# Примените этот декоратор к функции `say_hi`, которая выводит в консоль строку "Hi!".
# После применения декоратора вызов `say_hi()` должен привести к троекратному выводу "Hi!".

def repeat_three_times(func):
    def wrapper():
        for i in range(3):
            func()
    return wrapper

@repeat_three_times
def say_hi():
    print("Hi!")
say_hi()

