# Задача 14: Декораторы
# Создайте декоратор с именем `add_exclamation`.
# Декоратор должен модифицировать результат декорируемой функции, добавляя к нему восклицательный знак.
# Предполагается, что декорируемая функция возвращает строку.
# Внутри декоратора:
#   1) Получите результат вызова оригинальной функции.
#   2) Добавьте к этому результату "!" в конце.
#   3) Верните новую строку.
# Примените декоратор к функции `say_word`, которая возвращает строку "Привет".
# Вызовите декорированную функцию и выведите её результат.
def add_exclamation(func):
    def wrapper():
        original_result = func()
        new_result = original_result + "!"
        return new_result
    return wrapper
@add_exclamation
def say_word():
    return ("Привет")
print(say_word())

