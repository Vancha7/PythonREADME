# Создайте функцию с именем `calculate`.
# Функция должна принимать три числовых аргумента: `a`, `b` и `operation`.
# Аргумент `operation` — строка, которая может быть "+" или "-".
# Функция должна ВОЗВРАЩАТЬ (return) результат выполнения указанной операции над числами a и b.
# Если операция не "+" и не "-", функция должна возвращать строку "Неизвестная операция".
# Вызовите функцию три раза и выведите результаты:
# 1) a=5, b=3, operation="+"
# 2) a=10, b=7, operation="-"
# 3) a=2, b=2, operation="*"

def calculate(a, b, operation):
    if operation == "+":
        return a + b
    elif operation == "-":
        return a - b
    else:
        return "Неизвестная операция"
print(calculate(5, 3, "+"))
print(calculate(10, 7, "-"))
print(calculate(2, 2, "*"))

