# 1. Удали элемент "зеленый" по его значению.
# 2. Удали третий элемент списка по индексу (после первого удаления список изменился — учитывай это!).
# 3. Удали последний элемент методом pop().
# Выведи итоговый список на экран.
colors = ["красный", "синий", "зеленый", "желтый", "черный", "белый"]
colors.remove("зеленый")
del colors[2]
colors.pop(-1)
print(colors)