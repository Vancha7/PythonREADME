# Задача 7: Работа с файлами (чтение и вывод отформатированной информации)
# В файле 'users.txt' каждая строка имеет формат: 'имя,возраст,город'.
# Пример строки: 'Анна,25,Москва'.
# Прочитайте файл, для каждой строки выведите информацию в формате:
# 'Пользователь: [имя], возраст: [возраст], город: [город]'.
# Используйте контекстный менеджер with.
with open("users.txt", "r", encoding="utf-8") as file:
    for line in file:
        line = line.strip()
        parts = line.split(",")
        print(f'Пользователь: {parts[0]}, возраст: {parts[1]}, город: {parts[2]}')

