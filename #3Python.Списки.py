# Задача 1 | Получение элементов списка
# Дан список: fruits = ["яблоко", "банан", "апельсин", "киви", "манго"]
# 1. Выведи на экран второй элемент этого списка.
# 2. Выведи на экран предпоследний элемент этого списка.
# Вывод каждого элемента должен быть на отдельной строке.
fruits = ["яблоко", "банан", "апельсин", "киви", "манго"]
result1 = fruits[1]
result2 = fruits[-2]
print(result1)
print(result2)

# Задача 2 | Добавление элементов в список и их замена
