# Задача 3 | Срезы списка
# Дан список: data = ["a", "b", "c", "d", "e", "f", "g"]
# Используя срезы, получи новый список, содержащий элементы со второго по пятый включительно (счёт с нуля).
# Сохрани результат в переменную slice_result и выведи её на экран.
data = ["a", "b", "c", "d", "e", "f", "g"]
slice_result = data[1:5]
print(slice_result)

# Задача 4 | Удаление элементов из списка и его очистка
# Дан список: items = ["стол", "стул", "лампа", "книга", "чашка", "стул"]
# 1. Удали первый элемент этого списка по его индексу.
# 2. Удали элемент "стул" по его значению (только первое вхождение).
# 3. Очисти весь список, чтобы в нём не осталось элементов.
# После каждого шага выводи список на экран, чтобы видеть изменения.
items = ["стол", "стул", "лампа", "книга", "чашка", "стул"]
items.pop(0)
print(items)
items.remove("стул")
print(items)
items.clear()
print(items)

# Задача 5 | Слияние списков
# Даны два списка: list1 = [1, 2, 3] и list2 = [4, 5, 6]
# 1. Слей их в один новый список (в том же порядке) с помощью оператора +.
# 2. Результат сохрани в переменную merged и выведи на экран.
list1 = [1, 2, 3]
list2 = [4, 5, 6]
merged = list1 + list2
print(merged)

